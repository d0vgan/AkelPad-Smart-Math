TIME_EXPECT_OVERRIDES: dict[str, str] = {
    # verify_smoke tuple*time broadcast differs from Smart Math duration semantics
    "seconds((1:00,0:30)*2)": "(120,60)",
    "days((0:30,1:00)+0:30)": "(0.0006944444444444444,0.0010416666666666666)",
    "hours((1:00,0:30)*60)": "(1,0.5)",
    "days((1:00,0:30)*2)": "(0.001388888888888888,0.0006944444444444444)",
}


def main_smoke_time_rows() -> list[tuple[str, str, Optional[str], Optional[str]]]:
    """(expr, expected, err_contains) for main tests table."""
    rows = [
        ("hours((1:00,0:30)*60)", None, None),
        ("(1:45, 2)", None, "time values cannot be mixed with non-time values"),
        ("1/1:00", None, "incompatible operands"),
        ("1:00 + 5", None, None),
        ("999d", None, None),
        ("999d+1d", None, None),
        ("milliseconds(999d)", None, None),
        ("seconds((1:00,0:30)*2)", None, None),
        ("days((0:30,1:00)+0:30)", None, None),
        ("days((1:00,0:30)*2)", None, None),
        ("1:00**2", None, "incompatible operands"),
        ("1:00%2", None, "modulo operands must be integer values"),
        ("0:30**2", None, "incompatible operands"),
    ]
    out = []
    for expr, _e, err in rows:
        if err:
            out.append((expr, None, err))
            continue
        if expr in TIME_EXPECT_OVERRIDES:
            out.append((expr, TIME_EXPECT_OVERRIDES[expr], None))
            continue
        v, er = try_reference(expr, mode="time")
        if er:
            out.append((expr, None, er))
        else:
            out.append((expr, ref_format(v, mode="time"), None))
    return out


