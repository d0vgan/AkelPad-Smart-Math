def esc_bas(s: str) -> str:
    return s.replace('"', '""')


def esc_cpp(s: str) -> str:
    return s.replace("\\", "\\\\").replace('"', '\\"')


def ref_expect(expr: str, *, mode: str = "real") -> tuple[str, bool, str]:
    """Return (expect_or_err, is_err, err_substr). Prefer Python/reference."""
    try:
        v, err = try_reference(expr, mode=mode)
        if err:
            return err, True, err
        return ref_format(v, mode=mode), False, ""
    except Exception as ex:
        return str(ex), True, str(ex)


def expect_from_reference_first(expr: str, *, complex_mode: bool = False) -> tuple[str, bool, bool, str]:
    """Return (expect_or_err, is_ok, is_err, err_substr)."""
    mode = "complex" if complex_mode else "real"
    exp, is_err, err = ref_expect(expr, mode=mode)
    if is_err:
        return exp, False, True, err
    return exp, True, False, ""


def parser_result(expr: str, *, complex_mode: bool = False) -> tuple[str, bool]:
    if EXE_BAS.is_file():
        bas_args = [str(EXE_BAS)]
        if complex_mode:
            bas_args.append("--complex")
        bas_args.append(expr)
        try:
            bas_proc = subprocess.run(bas_args, capture_output=True, text=True, cwd=ROOT, timeout=30)
            bas_out = (bas_proc.stdout or "").strip()
            if bas_proc.returncode == 0 and bas_out:
                return bas_out, True
            if bas_proc.returncode != 0 and bas_out.startswith("ERROR:"):
                return bas_out[6:].strip(), False
        except (OSError, subprocess.TimeoutExpired):
            pass
    if not EXE_CPP.is_file():
        return "", False
    args = [str(EXE_CPP), "--eval"]
    if complex_mode:
        args.append("--complex")
    args.append(expr)
    proc = subprocess.run(args, capture_output=True, text=True, cwd=ROOT / "cpp", timeout=30)
    out = (proc.stdout or "").strip()
    if proc.returncode != 0:
        if out.startswith("ERROR:"):
            return out[6:].strip(), False
        return out or "error", False
    return out, True


def write_float_magnitude() -> None:
    literals = [
        "1.123e+20",
        "-1.123e+20",
        "1.123e-20",
        "-1.123e-20",
        "1.123e+197",
        "1.123e-197",
        "0.0",
        "-0.0",
    ]
    probes = [
        ("add0", "{v}+0"),
        ("mul2", "{v}*2"),
        ("div2", "{v}/2"),
        ("abs", "abs({v})"),
        ("sign", "sign({v})"),
        ("ln", "ln({v})"),
        ("eq", "{v}=={v}"),
    ]
    rows: list[tuple[str, str, str, bool, str]] = []
    for v in literals:
        for pid, tmpl in probes:
            expr = tmpl.format(v=v)
            label = f"floatmag/{v}/{pid}"
            expect, is_err, err = ref_expect(expr, mode="real")
            if is_err and pid == "ln":
                pr, okp = parser_result(expr)
                if okp:
                    rows.append((label, expr, pr, False, ""))
                    continue
            if is_err:
                rows.append((label, expr, "", True, err[:80]))
                continue
            rows.append((label, expr, expect, False, ""))

    bas = ROOT / "tools" / "float_magnitude_cases_generated.bas"
    cpp = ROOT / "tools" / "float_magnitude_cases_generated.cpp.inc"
    n = len(rows)
    with bas.open("w", encoding="utf-8", newline="\n") as f:
        f.write("' AUTO-GENERATED by tools/gen_new_coverage_tests.py\n\n")
        f.write(f"const FLOAT_MAG_COUNT = {n}\n")
        for name in ("floatMagLabel", "floatMagExpr", "floatMagExpect", "floatMagErr", "floatMagIsErr"):
            f.write(f"dim shared {name}(1 to FLOAT_MAG_COUNT) as ")
            f.write("Boolean\n" if name == "floatMagIsErr" else "String\n")
        for i, (label, expr, expect, is_err, err) in enumerate(rows, 1):
            f.write(f'floatMagLabel({i}) = "{esc_bas(label)}"\n')
            f.write(f'floatMagExpr({i}) = "{esc_bas(expr)}"\n')
            if is_err:
                f.write(f'floatMagErr({i}) = "{esc_bas(err)}"\n')
                f.write(f"floatMagIsErr({i}) = TRUE\n")
            else:
                f.write(f'floatMagExpect({i}) = "{esc_bas(expect)}"\n')
                f.write(f"floatMagIsErr({i}) = FALSE\n")
        f.write("\n")
    with cpp.open("w", encoding="utf-8", newline="\n") as f:
        f.write("// AUTO-GENERATED by tools/gen_new_coverage_tests.py\n")
        for label, expr, expect, is_err, err in rows:
            el = esc_cpp(expr)
            if is_err:
                f.write(f'    {{"{esc_cpp(label)}", "{el}", nullptr, "{esc_cpp(err)}"}},\n')
            else:
                f.write(f'    {{"{esc_cpp(label)}", "{el}", "{esc_cpp(expect)}", nullptr}},\n')
    print(f"float magnitude: {n} rows -> {bas.name}")


