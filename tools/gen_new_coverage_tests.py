def write_complex_coverage() -> None:
    rows: list[tuple[str, str, Optional[str], Optional[str], bool]] = []

    def add_probe(label: str, expr: str) -> None:
        """Prefer Python/reference for successes; fall back to parser eval."""
        exp, ok, _is_err, _err = expect_from_reference_first(expr, complex_mode=True)
        if ok:
            rows.append((label, expr, exp, None, False))
            return
        pr, pok = parser_result(expr, complex_mode=True)
        if pok:
            rows.append((label, expr, pr, None, False))
            return
        rows.append((label, expr, None, pr or "error", True))

    def add_forbid_probe(label: str, expr: str) -> None:
        """Forbidden ops: lock error text to parser (Basic smoke), not Python exceptions."""
        pr, pok = parser_result(expr, complex_mode=True)
        if pok:
            rows.append((label, expr, pr, None, False))
            return
        rows.append((label, expr, None, pr or "error", True))

    def add_probe_parser_first(label: str, expr: str) -> None:
        pr, pok = parser_result(expr, complex_mode=True)
        if pok:
            rows.append((label, expr, pr, None, False))
            return
        exp, _ok, _is_err, err = expect_from_reference_first(expr, complex_mode=True)
        if _ok:
            rows.append((label, expr, exp, None, False))
            return
        rows.append((label, expr, None, pr or err or "error", True))

    # C5 doc utilities (lock to parser format for cross-platform ULP parity)
    for expr in ("sign(1+2i)", "(1+2i)/abs(1+2i)", "fact(1+2i)"):
        add_probe_parser_first(f"cxdoc/{expr}", expr)

    operands = ["nan+2i", "nan+3i", "2+nan*i", "inf+2i", "nan+nan*i", "inf+nan*i", "nan*inf"]
    op_templates = [
        ("add", "{z}+1"),
        ("sub", "{z}-1"),
        ("mul", "{z}*2"),
        ("div", "{z}/2"),
        ("pow", "{z}**2"),
        ("uplus", "+({z})"),
        ("uminus", "-({z})"),
        ("eq_self", "{z}=={z}"),
        ("neq", "{z}!=1+2i"),
        ("and", "({z})&&1"),
        ("or", "({z})||0"),
        ("not", "not({z})"),
    ]
    forbidden = [
        ("pct", "({z})%3"),
        ("lt", "{z}<{z}"),
        ("shl", "{z}<<1"),
        ("band", "{z}&1"),
    ]
    for z in operands:
        for op_id, tmpl in op_templates:
            expr = tmpl.format(z=z)
            add_probe(f"cxnf/{z}/{op_id}", expr)
        for op_id, tmpl in forbidden:
            expr = tmpl.format(z=z)
            add_forbid_probe(f"cxnf/forbid/{z}/{op_id}", expr)

    add_probe("cxnf/eq_nan2i", "(nan+2i)==(nan+2i)")
    add_probe("cxnf/sortby_abs", "sortby((1+2i, 3-i), abs)")
    # Lexer/decimal exact-int preservation: lock to parser output
    add_probe_parser_first("cxlex/bare_int64min", "-9223372036854775808+1i")
    add_probe_parser_first("cxlex/paren_int64min", "(-9223372036854775808)+1i")

    bas = ROOT / "tools" / "complex_coverage_generated.bas"
    cpp = ROOT / "tools" / "complex_coverage_generated.cpp.inc"
    n = len(rows)
    with bas.open("w", encoding="utf-8", newline="\n") as f:
        f.write("' AUTO-GENERATED by tools/gen_new_coverage_tests.py\n\n")
        f.write(f"const CX_COVERAGE_COUNT = {n}\n")
        for p in ("cxCovLabel", "cxCovExpr", "cxCovExpect", "cxCovErr", "cxCovIsErr"):
            f.write(f"dim shared {p}(1 to CX_COVERAGE_COUNT) as ")
            f.write("Boolean\n" if p == "cxCovIsErr" else "String\n")
        for i, (label, expr, expect, err, is_err) in enumerate(rows, 1):
            f.write(f'cxCovLabel({i}) = "{esc_bas(label)}"\n')
            f.write(f'cxCovExpr({i}) = "{esc_bas(expr)}"\n')
            if is_err:
                f.write(f'cxCovErr({i}) = "{esc_bas(err or "")}"\n')
                f.write(f"cxCovIsErr({i}) = TRUE\n")
            else:
                f.write(f'cxCovExpect({i}) = "{esc_bas(expect or "")}"\n')
                f.write(f"cxCovIsErr({i}) = FALSE\n")
    with cpp.open("w", encoding="utf-8", newline="\n") as f:
        f.write("// AUTO-GENERATED by tools/gen_new_coverage_tests.py\n")
        for label, expr, expect, err, is_err in rows:
            el = esc_cpp(expr)
            if is_err:
                f.write(
                    f'    {{"{esc_cpp(label)}", "{el}", nullptr, "{esc_cpp(err or "")}"}},\n'
                )
